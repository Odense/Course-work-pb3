void ATest_cursachPawn::NotifyHit(class UPrimitiveComponent* MyComp, AActor* Other, class UPrimitiveComponent* OtherComp,
	bool bSelfMoved, FVector HitLocation, FVector HitNormal, FVector NormalImpulse, const FHitResult& Hit)
{
	Super::NotifyHit(MyComp, Other, OtherComp, bSelfMoved, HitLocation, HitNormal, NormalImpulse, Hit);

	float RecievedDamage = NormalImpulse.Size() / ImpulseToDamage;

	TakeDamage(RecievedDamage, FDamageEvent(), GetController(), Other);

	if (GetWorld()->GetTimeSeconds() - LastSoundHitTime > TimeBetweenHitSounds) 
	{
		///TODO: export constants to UPROPERTY
		float t = NormalImpulse.Size() / 7000000.0f;
		t = FMath::Clamp<float>(t, 0.5, 1);
		//t = FMath::Clamp(t, 0.5f, 1.0f);

		UGameplayStatics::PlaySoundAtLocation(GetWorld(), HitSound, HitLocation, t);

		LastSoundHitTime = GetWorld()->GetTimeSeconds();
	}
}

float ATest_cursachPawn::TakeDamage(float Damage, struct FDamageEvent const& DamageEvent, AController* EventInstigator, AActor* DamageCauser)
{
	Super::TakeDamage(Damage, DamageEvent, EventInstigator, DamageCauser);

	HP -= Damage;

	//UE_LOG(LogTemp, Warning, TEXT("%f %f"), Damage, HP);

	return Damage;
}
